package thuvien;

public class thuvien {
	
	private int matl;
	private String tennsx;
	private int sobanph;
	public thuvien(int matl, String tennsx, int sobanph) {
		super();
		this.matl = matl;
		this.tennsx = tennsx;
		this.sobanph = sobanph;
	}
	public int getMatl() {
		return matl;
	}
	public void setMatl(int matl) {
		this.matl = matl;
	}
	public String getTennsx() {
		return tennsx;
	}
	public void setTennsx(String tennsx) {
		this.tennsx = tennsx;
	}
	public int getSobanph() {
		return sobanph;
	}
	public void setSobanph(int sobanph) {
		this.sobanph = sobanph;
	}

	}

